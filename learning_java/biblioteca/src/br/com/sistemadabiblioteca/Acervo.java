package br.com.sistemadabiblioteca;

public class Acervo {
	
	private Integer codBarras;
	private String titulo;
	private Integer codEstante;
	private Integer codPrat;
	
	public Integer getCodBarras() {
		return codBarras;
	}
	public void setCodBarras(Integer codBarras) {
		this.codBarras = codBarras;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public Integer getCodEstante() {
		return codEstante;
	}
	public void setCodEstante(Integer codEstante) {
		this.codEstante = codEstante;
	}
	public Integer getCodPrat() {
		return codPrat;
	}
	public void setCodPrat(Integer codPrat) {
		this.codPrat = codPrat;
	}



}
